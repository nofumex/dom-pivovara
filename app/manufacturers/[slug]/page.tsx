export default function ManufacturerPage({
  params,
}: {
  params: { slug: string }
}) {
  const manufacturer = manufacturers[params.slug as keyof typeof manufacturers]

  if (!manufacturer) {
    notFound()
  }

  const breadcrumbs = [
    { label: 'Главная', href: '/' },
    { label: 'Производители', href: '/manufacturers' },
    { label: manufacturer.name, href: `/manufacturers/${params.slug}` },
  ]

  return (
    <main>
      <div className="container">
        <Breadcrumbs items={breadcrumbs} />
        <div className={styles.content}>
          <h1 className={styles.title}>{manufacturer.name}</h1>
          <div className={styles.imageWrapper}>
            <img
              src={manufacturer.image}
              alt={manufacturer.name}
              className={styles.logo}
            />
          </div>
          <div className={styles.description}>
            {manufacturer.description.split('\n\n').map((paragraph, index) => (
              <p key={index}>{paragraph}</p>
            ))}
          </div>
        </div>
      </div>
    </main>
  )
}

